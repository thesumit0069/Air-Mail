    public void ping_mailActionPerformed(java.awt.event.ActionEvent evt) 
    {//GEN-FIRST:event_ping_mailActionPerformed
        
         
        
 		String sender_id , receiver_id , subject, content  ="";

        sender_id =  from_id.getText();
        receiver_id=to_id.getText();
        subject= sub.getText();
  		 content= heart.getText();

        
        
          final  char getpass[] = get_passcode.getPassword();								//fetching  passcode from textfield
          
          final  String  passcode = String.valueOf(getpass);
        
                                                                           
 
		    Authenticator auth = new Authenticator() {										//Checking Credentials
            	
                // override the getPasswordAuthentication method
                protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(sender_id,passcode);
                }
        };





        																				 //   Mail Hosting Provided by     [Gmail] 	

          Properties props = new Properties();    										//Setting Properties of for Sending Mail  
              props.put("mail.smtp.host", "smtp.gmail.com");    
              props.put("mail.smtp.socketFactory.port", "465");    
              props.put("mail.smtp.socketFactory.class",    
                        "javax.net.ssl.SSLSocketFactory");    
              props.put("mail.smtp.auth", "true");    
              props.put("mail.smtp.port", "465");  

        Session session = Session.getDefaultInstance(props, auth);





   
   try {    
 

               MimeMessage message = new MimeMessage(session);    
 

		       message.addRecipient(Message.RecipientType.TO,new InternetAddress(receiver_id));   
	      

       	       message.setSubject(subject);																	//Setting 	[Subject of Mail]			    
               
               
            


			MimeBodyPart messageBodyPart1 = new MimeBodyPart();  
    		messageBodyPart1.setText(content);																//Setting    [Body Part] 

    
    	  MimeBodyPart bodypart2 = new MimeBodyPart();  	
	
		    bodypart2.setDataHandler(new DataHandler(source));  
			bodypart2.attachFile(file_path);   															// Attaching  [file]				



	     
	        Multipart   multipart = new MimeMultipart();  
	    	multipart.addBodyPart(messageBodyPart1);  
	    	multipart.addBodyPart(bodypart2);  






               message.setContent(multipart);  																//Setting Entire Mail Content

               Transport.send(message);    																	//	[Sending Mail]
               System.out.println("\n Message sent successfully : ");
               
		   
		

		        sub.setText("");											//Clearing Entered Data of fields :  Sub,Heart,Recipients_id  &  Attached File 
		        heart.setText("");
		        to_id.setText("");
		        attach_file.setText("Attach File !");
        
             
  
	
} 

	catch (Exception e) {throw new RuntimeException(e);}

         
         
        
    }																			//Ping_Mail Method END								
